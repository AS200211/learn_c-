#include<iostream>
#include<cmath>
using namespace std;

int main(){

    float var1,var2;

    var1 = 13.35434;
    var2 = 64.567;

    cout << "Floor value of var1: " << floor(var1) << endl;
    cout << "Floor value of var2: " << floor(var2) << endl;
    cout << "Ceil value of var1: " << ceil(var1) << endl;
    cout << "Ceil value of var1: " << ceil(var2) << endl;
    cout << "Round of value of var1: " << round(var1) << endl;
    cout << "Round of value of var1: " << round(var2) << endl;

    return 0;
}